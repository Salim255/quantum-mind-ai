# rag_eval_schema.py
# ---------------------------------------------------------
# PURPOSE
# -------
# Store evaluation data for every RAG request.
#
# WHY THIS MATTERS
# ----------------
# RAG systems are difficult to improve blindly.
#
# Logging lets us measure:
# - retrieval quality
# - reranking quality
# - hallucinations
# - latency
# - chunk usefulness
#
# This schema becomes the foundation
# of the RAG evaluation dashboard.
# ---------------------------------------------------------

from typing import List
from pydantic import BaseModel, Field, Field
from app.v1.modules.rag.dto.retrieval_dto import RetrievalChunkDTO




class RAGEvaluationLogDto(BaseModel):
    """
    Full evaluation log for a single RAG query.
    """

    # -----------------------------------------------------
    # USER INPUT
    # -----------------------------------------------------
    query: str = Field(..., description="The original user query that triggered the RAG process")

    # -----------------------------------------------------
    # RETRIEVAL
    # -----------------------------------------------------
    retrieved_chunks: List[RetrievalChunkDTO]

    # -----------------------------------------------------
    # FINAL GENERATED ANSWER
    # -----------------------------------------------------
    final_answer: dict = Field(..., description="The final answer generated by the RAG system")

    # -----------------------------------------------------
    # PERFORMANCE
    # -----------------------------------------------------
    latency_ms: float = Field(..., description="The latency of the RAG process in milliseconds")

    # -----------------------------------------------------
    # MODEL INFO
    # -----------------------------------------------------
    model: str = Field(..., description="The model name you are using")

    # -----------------------------------------------------
    # OPTIONAL METADATA
    # -----------------------------------------------------
    top_k: int = Field(..., description="The number of top chunks to retrieve")